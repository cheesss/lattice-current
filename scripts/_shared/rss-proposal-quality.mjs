function unique(values = []) {
  return Array.from(new Set(values.filter(Boolean)));
}

export function normalizeRssProposalText(value) {
  return String(value || '')
    .trim()
    .toLowerCase()
    .replace(/\+/g, ' ')
    .replace(/[_/]+/g, ' ')
    .replace(/\b(?:when|before|after|source|site|intitle):[^\s]+/g, ' ')
    .replace(/["'`]+/g, ' ')
    .replace(/[^a-z0-9+\-\s]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

export function isMeaningfulRssSearchTerm(value) {
  const normalized = normalizeRssProposalText(value);
  if (!normalized) return false;
  if (normalized.length < 4) return false;
  if (/^\d+$/.test(normalized)) return false;
  if (BLOCKED_RSS_QUERY_TERMS.has(normalized)) return false;
  const tokens = normalized.split(' ').filter(Boolean);
  if (tokens.length === 0) return false;
  const meaningfulTokens = tokens.filter((token) => (
    token.length >= 4
    && !BLOCKED_RSS_QUERY_TERMS.has(token)
  ));
  if (tokens.length > 1) return meaningfulTokens.length > 0;
  return meaningfulTokens.length === 1;
}

export function extractRssSearchQuery(payload = {}) {
  const querySummary = normalizeRssProposalText(payload.querySummary || '');
  if (querySummary) return querySummary;
  const url = String(payload.url || '').trim();
  if (!url) return '';
  try {
    const parsed = new URL(url);
    const q = parsed.searchParams.get('q');
    return normalizeRssProposalText(q || '');
  } catch {
    return '';
  }
}

export function extractMeaningfulRssSearchTerms(payload = {}) {
  const query = extractRssSearchQuery(payload);
  if (!query) return [];
  const tokens = query
    .split(/\s+/)
    .map((token) => normalizeRssProposalText(token))
    .filter(Boolean)
    .filter((token) => !['and', 'or', 'not'].includes(token));
  const phrase = normalizeRssProposalText(query);
  const candidates = [];
  if (isMeaningfulRssSearchTerm(phrase)) candidates.push(phrase);
  for (const token of tokens) {
    if (isMeaningfulRssSearchTerm(token)) candidates.push(token);
  }
  return unique(candidates);
}

export function isAutoGeneratedGoogleNewsRssProposal(proposal = {}) {
  const payload = proposal.payload && typeof proposal.payload === 'object'
    ? proposal.payload
    : proposal;
  const url = String(payload.url || '').trim();
  if (!url) return false;
  try {
    const parsed = new URL(url);
    if (!/news\.google\.com$/i.test(parsed.hostname)) return false;
  } catch {
    return false;
  }
  const source = normalizeRssProposalText(proposal.source || '');
  const reasoning = normalizeRssProposalText(proposal.reasoning || payload.reason || '');
  return source.includes('topic discovery')
    || source.includes('topic-discovery')
    || reasoning.includes('auto generated')
    || reasoning.includes('auto-generated')
    || reasoning.includes('topic discovery')
    || reasoning.includes('topic-discovery');
}

export function classifyRssProposalSignal(proposal = {}) {
  const payload = proposal.payload && typeof proposal.payload === 'object'
    ? proposal.payload
    : proposal;
  const query = extractRssSearchQuery(payload);
  const meaningfulTerms = extractMeaningfulRssSearchTerms(payload);
  const autoGeneratedGoogleNews = isAutoGeneratedGoogleNewsRssProposal(proposal);
  return {
    query,
    meaningfulTerms,
    autoGeneratedGoogleNews,
    lowSignal: autoGeneratedGoogleNews && meaningfulTerms.length === 0,
  };
}

export function isLowSignalAddRssProposal(proposal = {}) {
  const proposalType = String(
    proposal.proposal_type
    || proposal.proposalType
    || proposal.type
    || '',
  ).trim().toLowerCase();
  if (proposalType !== 'add-rss') return false;
  return classifyRssProposalSignal(proposal).lowSignal;
}
